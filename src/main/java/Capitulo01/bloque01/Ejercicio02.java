package Capitulo01.bloque01;

import java.util.Scanner;

public class Ejercicio02 {

	public static void main(String[] args) {
		
			Scanner sc = new Scanner(System.in); // Lee el teclado
	
			
			int numEntero = sc.nextInt();
			System.out.println("Introduzca un número:" + numEntero);
			
			float numFloat = sc.nextFloat();
			System.out.println("Introduzca un número flotante:" + numFloat);

			double numDouble = sc.nextDouble();
			System.out.println("Introduzca un número doble:" + numDouble);


	}

}
